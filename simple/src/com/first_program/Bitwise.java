package com.first_program;

public class Bitwise {
    public static void main(String[] args) {
        byte x,y,z;
        x=10;
        y=11;
        z=-10;
        System.out.println("~x="+(~x));
        System.out.println("x&y="+(x&y));
        System.out.println("x|y="+(x|y));
        System.out.println("x^y="+(x^y));
        System.out.println("x>>2="+(x>>2));
        System.out.println("x>>>2"+(x>>>2));
        System.out.println("x<<2="+(x<<2));
        System.out.println(("z<<2="+(z<<2)));
        System.out.println("z>>2="+(z>>2));
        System.out.println("z>>>2="+(z>>>2));


    }
}
