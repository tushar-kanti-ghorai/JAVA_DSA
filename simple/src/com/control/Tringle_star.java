package com.control;

public class Tringle_star {
    public static void main(String[] args){
        for(int i=1;i<=5;i++){//row
            for(int j=1;j<=i;j++){//col
                System.out.print("* ");
            }
//            System.out.print("\n");
            System.out.println();
        }
    }
}
